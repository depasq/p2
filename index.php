        <h2 class="content-subhead">Dynamic Web Applications</h2>
        <p>
          More and more of the projects that I work on involve some web-based component. Perhaps my most favorite project is the <a href="http://chandra.si.edu/photo/map/index.html" target="_blank">Chandra Sky Map</a>, an interactive map for exploring
          Chandra's press release imagery built in javascript using the D3 data visualization library. I'm interested in expanding my current skill set and learning the latest and greatest web technologies to provide new avenues for innovation and interactivity
          in astronomical public outreach.
        </p>

